static bool pause(const char *prompt, int millisec)
{
	int i = 0;
	print(prompt);
	for (i = 0; i < millisec; ++i) {
		delay_us(1000);
		if (serial_input_available()) {
			char c = 0;
			c = serial_input_char();
			if ('\r' == c || '\n' == c) {
				return true;
			}
			c = serial_input_char();
			if ('\r' == c || '\n' == c) {
				return true;
			}
			break;
		}
	}
	return false;
}


static void fill(u8 value)
{
	int x = 0;
	int y = 0;
	u8 *fb = (u8*)LCD_VRAM;
	for (y = 0; y < LCD_HEIGHT_LINES; ++y) {
		for (x = 0; x < LCD_VRAM_WIDTH_BYTES; ++x) {
			*fb++ = value;
		}
	}
}

static void stripe(u8 value, u8 fill)
   {
	int x = 0;
	int y = 0;
	u8 *fb = (u8*)LCD_VRAM;
	for (y = 0; y < LCD_HEIGHT_LINES; ++y) {
		for (x = 0; x < LCD_VRAM_WIDTH_BYTES; ++x) {
			*fb++ = (0 == (y & (1 << 5))) ? value : fill;
		}
	}
}

static void display_image(u8 toggle)
{
	int x = 0;
	const u8 *src = image_data;
	u8 *fb = (u8*)LCD_VRAM;
	int l = 0;

	fill(toggle);
	for (l = 0; l < sizeof(image_data); ++l) {
		fb[x] = toggle ^ *src++;
		++x;
		if (LCD_WIDTH_BYTES <= x) {
			x = 0;
			fb += LCD_VRAM_WIDTH_BYTES;
		}
	}
}


static bool process(void)
{

	fill(0);
	if (pause("00 ", DELAY)) {
		return false;
	}

	fill(0xff);
	if (pause("ff ", DELAY)) {
		return false;
	}

	fill(0x55);
	if (pause("55 ", DELAY)) {
		return false;
	}

	fill(0x44);
	if (pause("44 ", DELAY)) {
		return false;
	}

	fill(0xf7);
	if (pause("f7 ", DELAY)) {
		return false;
	}
	print_char('\n');

	print("stripe(00) ");

	stripe(0xff, 0x00);
	if (pause("ff ", DELAY)) {
		return false;
	}

	stripe(0x01, 0x00);
	if (pause("01 ", DELAY)) {
		return false;
	}
	print_char('\n');

	print("stripe(ff) ");

	stripe(0x00, 0xff);
	if (pause("00 ", DELAY)) {
		return false;
	}

	stripe(0xee, 0xff);
	if (pause("7f ", DELAY)) {
		return false;
	}
	print_char('\n');

	display_image(0x00);
	if (pause("image(00)", DELAY)) {
		return false;
	}
	print_char('\n');

	display_image(0xff);
	if (pause("image(ff)", DELAY)) {
		return false;
	}
	print_char('\n');

	return true;
}
